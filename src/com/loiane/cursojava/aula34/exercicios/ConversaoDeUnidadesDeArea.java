package com.loiane.cursojava.aula34.exercicios;

public class ConversaoDeUnidadesDeArea {

	public static double metroQuadradoParaPeQuadrado(double metro) {
		return metro * 10.76;
	}
	
	public static double peQuadradoParaCentQuadrado(double pe) {
		return pe * 929;
	}
	
	public static double milhaQuadradaParaAcre(double milha) {
		return milha * 640;
	}
	
	public static double acreParaPesQuadrados(double acre) {
		return acre * 43560;
	}
	
}
