package com.loiane.cursojava.aula13;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		int v1 = 1;
		int v2 = 2;
		
		System.out.println(v1==v2);
		System.out.println(v1!=v2);
		System.out.println(v1>=v2);
		System.out.println(v1<=v2);
		System.out.println(v1>v2);
		System.out.println(v1<v2);
		
	}

}
